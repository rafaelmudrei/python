letra = input("Digite a letra (A-G): ")

if letra == 'A':
    print("Muito eficiente")
elif letra == 'B':
    print("Eficiente")
elif letra == 'C':
    print("Razoável")
elif letra == 'D':
    print("Pouco eficiente")
elif letra == 'E':
    print("Ineficiente")
elif letra == 'F':
    print("Muito ineficiente")
elif letra == 'G':
    print("Péssima eficiência")
else:
    print("Letra inválida")